print("__________Menghitung Diameter, Keliling, dan Luas Lingkaran__________\n")
s= int(input('Masukkan Nilai Radiusnya (r) : '))
diameter = 2 * s
keliling = (2 * 22 * s)/7
luas = (22 * (s ** 2 ))/7



print("\nNilai nilai sisi yang kamu masukkan : ", s)
print("Diameter lingkaran tersebut : ", diameter)
print("Keliling lingkaran tersebut : ", keliling)
print("Luas lingkaran tersebut : ", luas)


